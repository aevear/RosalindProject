import math
#-------------------------------------------------------------------------------
# Pper
#-------------------------------------------------------------------------------
def runPper(inputFile):
    fi = open(inputFile, "r")
    inputData = fi.read().split(" ")
    n, k = int(inputData[0]), int(inputData[1])

    perm = (math.factorial(n)/math.factorial(n-k))


    return perm % 1000000

