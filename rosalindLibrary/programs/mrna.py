from rosalindLibrary.tools.rosalindTools import DnaCombinationTable
#-------------------------------------------------------------------------------
# GC
#-------------------------------------------------------------------------------

def runMrna(inputFile):
    fi = open(inputFile, 'r')
    dnaString = fi.read().strip("\n")
    totalLength = 1

    for k in dnaString:
        totalLength = totalLength * DnaCombinationTable(k)

    return (totalLength * 3) % 1000000


